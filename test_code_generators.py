def test_import_and_call_generator(module_path, function_name, test_params):
    """Test importing and calling a specific generator function."""
    print(f"\n{'='*60}")
    print(f"Testing: {module_path} -> {function_name}")
    print(f"{'='*60}")
    
    try:
        # Import the module
        module_name = module_path.replace('/', '.').replace('.py', '')
        if module_name.startswith('.'):
            module_name = module_name[1:]
        
        module = importlib.import_module(module_name)
        
        # Get the function
        if not hasattr(module, function_name):
            print(f"‚ùå FAILED: Function '{function_name}' not found in module")
            return False
        
        generator_func = getattr(module, function_name)
        print(f"‚úÖ Successfully imported {function_name}")
        
        # Call the function with test parameters
        generated_code = generator_func(test_params)
        
        # Verify it returns a string
        if not isinstance(generated_code, str):
            print(f"‚ùå FAILED: Function returned {type(generated_code)}, expected string")
            return False
        
        print(f"‚úÖ Function returned a string ({len(generated_code)} characters)")
        
        # Verify it's not empty
        if not generated_code.strip():
            print(f"‚ùå FAILED: Generated code is empty")
            return False
        
        # Count lines (excluding empty lines)
        lines = [line for line in generated_code.split('\n') if line.strip()]
        line_count = len(lines)
        print(f"‚úÖ Generated code has {line_count} non-empty lines")
        
        # Check if it's in simplified style (should be around 19-26 lines, not 160+)
        if line_count > 50:
            print(f"‚ö†Ô∏è  WARNING: Code seems too long ({line_count} lines) - may not be simplified style")
        elif line_count < 15:
            print(f"‚ö†Ô∏è  WARNING: Code seems too short ({line_count} lines) - may be incomplete")
        else:
            print(f"‚úÖ Code length looks appropriate for simplified style")
        
        # Check for expected elements
        expected_elements = [
            'from core.designs',  # Import statement
            'def ',               # Should not contain function definitions (generated code should be standalone)
            'result =',           # Should have result assignment
            'print(',             # Should have print statements
            'json.dumps',         # Should have JSON output
            '# Generated by DesignPower'  # Should have header comment
        ]
        
        print("\nChecking for expected code elements:")
        for element in expected_elements:
            if element == 'def ' and element in generated_code:
                print(f"‚ö†Ô∏è  WARNING: Found 'def ' - generated code should not contain function definitions")
            elif element == 'def ':
                print(f"‚úÖ No function definitions found (good)")
            elif element in generated_code:
                print(f"‚úÖ Found: {element}")
            else:
                print(f"‚ùå Missing: {element}")
        
        # Show first few lines of generated code
        print(f"\nFirst 10 lines of generated code:")
        print("-" * 40)
        for i, line in enumerate(generated_code.split('\n')[:10], 1):
            print(f"{i:2d}: {line}")
        
        # Try to write and parse the code (basic syntax check)
        try:
            compile(generated_code, '<generated_code>', 'exec')
            print(f"‚úÖ Generated code has valid Python syntax")
        except SyntaxError as e:
            print(f"‚ùå SYNTAX ERROR in generated code: {e}")
            return False
        
        print(f"‚úÖ {function_name} test PASSED")
        return True
        
    except Exception as e:
        print(f"‚ùå FAILED: {e}")
        traceback.print_exc()
        return False


